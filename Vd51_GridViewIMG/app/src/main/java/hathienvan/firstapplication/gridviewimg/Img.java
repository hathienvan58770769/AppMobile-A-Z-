package hathienvan.firstapplication.gridviewimg;

public class Img {
    private String ten;
    private int img;

    public Img(String ten, int img) {
        this.ten = ten;
        this.img = img;
    }

    public String getTen() {
        return ten;
    }

    public void setTen(String ten) {
        this.ten = ten;
    }

    public int getImg() {
        return img;
    }

    public void setImg(int img) {
        this.img = img;
    }
}

